    private void SignUpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpBtnActionPerformed
        // TODO add your handling code here:
            try {
            // TODO add your handling code here:
              con = DriverManager.getConnection("jdbc:mysql://localhost/primaryschool","root","");
            add  = (PreparedStatement) con.prepareStatement("insert into guardian (id,name,email,password,contact) Values (?,?,?,?,?)");
            add.setString(1, idTxt.getText());
            add.setString(2, nameTxt.getText());
            add.setString(3, emailTxt.getText());
            add.setString(4, passTxt.getText());
            add.setString(5, contactTxt.getText());
            int k  = add.executeUpdate();
            if(k == 1){
                JOptionPane.showMessageDialog(this, "SignUp Successful");
                cleanTxt();
                new GuardianLoginPage().setVisible(true);
                this.setVisible(false);
                
               
            }
            else{
                
                JOptionPane.showMessageDialog(this, "Error");
                cleanTxt();
                
            }
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(this, "This account for this id already exists");
            //Logger.getLogger(Teacher.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_SignUpBtnActionPerformed

